     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index( Buyer $buyer)
    {
        /**## obtenemos los vendedores apartir del comprador por lo cual pasamos el vendedor
        /*+ Laravel se encarga de resolver todas las lista de las transacciones junto con la lista de los productos que conforman cada una de ellas 
        *y luego el vendedor de cada uno de los productos **/
        $sellers = $buyer->transactions()->with('product.seller')->get()
        ->pluck('product.seller')
        /** el metodo unique conserva el valor de lo sindeces originales de la colleccion*/
        ->unique('id')
        /** reorganiza los indices eliminando los que se repiten */
        ->values();
    
        //dd($sellers);

        return $this->showAll($sellers);

    }

